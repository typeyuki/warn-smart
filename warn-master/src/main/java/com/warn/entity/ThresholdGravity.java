package com.warn.entity;

public class ThresholdGravity {
    private OldMan oldMan;
    private String times;
    private Integer id;
    private Integer thresholdGravity;

    public OldMan getOldMan() {
        return oldMan;
    }

    public void setOldMan(OldMan oldMan) {
        this.oldMan = oldMan;
    }

    public String getTimes() {
        return times;
    }

    public void setTimes(String times) {
        this.times = times;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getThresholdGravity() {
        return thresholdGravity;
    }

    public void setThresholdGravity(Integer thresholdGravity) {
        this.thresholdGravity = thresholdGravity;
    }
}
